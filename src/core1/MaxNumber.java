//Возврат максимального из двух чисел (Тернарный оператор)
//
//Напиши метод, который принимает два числа a и b, и возвращает
// максимальное из них, используя только тернарный оператор,
// метод .max(), а так же if() - НЕ ПОДОЙДЕТ!
public class MaxNumber {
    public static int isMax (int a, int b){
        return a>b?a:b;
    }
}
